;WRITE A PROGRAM IN ARM7TDMI TO FIND THE SUM OF ALL THE POSITIVE NUMBERS IN AN ARRAY
 
.DATA
A: .WORD 1,-3,0,-8,7,5,6
SUM: .WORD 0

.TEXT
LDR R0,=A  ;ARRAY
MOV R1,#0  ;INDEX VARIABLE
MOV R2,#0  ;SUM VARIABLE
LDR R4,=SUM

BL LOOP
	STR R2,[R4]
	SWI 0X011

LOOP: 
	LDR R3,[R0],#4
	MOVS R3,R3
	BPL POSITIVE
	
L1: ADD R1,R1,#1
	CMP R1,#10
	BNE LOOP
	MOV PC,LR
	
POSITIVE: ADD R2,R2,R3
		  B L1
		  
		  
		  
SWI 0X011
	

	
	